package tolkien.lotr.model;

public class Race {

	private Integer pkRace;
	private String raceName;

	public Integer getPkRace() {
		return pkRace;
	}

	public void setPkRace(Integer pkRace) {
		this.pkRace = pkRace;
	}

	public String getRaceName() {
		return raceName;
	}

	public void setRaceName(String raceName) {
		this.raceName = raceName;
	}

}